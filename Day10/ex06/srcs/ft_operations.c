int	plus(int a, int b)
{
	return (a + b);
}

int	minus(int a, int b)
{
	return (a - b);
}

int	devide(int a, int b)
{
	return (a / b);
}

int	multiply(int a, int b)
{
	return (a * b);
}

int	rem(int a, int b)
{
	return (a % b);
}
